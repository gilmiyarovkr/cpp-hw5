#pragma once

#include <vector>
#include <memory>
#include "IPrimitive.h"

/**
 * @class SceneModel
 * @brief Класс модели сцены, хранящий и управляющий графическими примитивами.
 *
 * Представляет собой контейнер (слой данных Model в паттерне MVC) для всех
 * объектов, унаследованных от `IPrimitive`. Обеспечивает централизованное
 * добавление, удаление и доступ к коллекции графических элементов.
 */
class SceneModel
{
private:
    /**
     * @brief Динамический массив умных указателей на графические примитивы.
     *
     * Использует `std::shared_ptr` для разделяемого владения объектами сцены,
     * что позволяет безопасно передавать их в подсистемы отображения и сериализации.
     */
    std::vector<std::shared_ptr<IPrimitive>> primitives;

public:
    /**
     * @brief Конструктор по умолчанию.
     */
    SceneModel() = default;

    /**
     * @brief Деструктор по умолчанию.
     *
     * Автоматически очищает вектор примитивов, освобождая выделенную под них память
     * (если на них больше нет внешних ссылок `std::shared_ptr`).
     */
    ~SceneModel() = default;

    /**
     * @brief Добавляет графический примитив на сцену.
     *
     * Принимает умный указатель на объект примитива. Если указатель не является пустым (`nullptr`),
     * он эффективно перемещается (`std::move`) внутрь внутреннего контейнера сцены.
     *
     * @param primitive Умный указатель `std::shared_ptr<IPrimitive>` на добавляемый объект.
     */
    void AddPrimitive(std::shared_ptr<IPrimitive> primitive)
    {
        if (primitive)
        {
            primitives.push_back(std::move(primitive));
        }
    }

    /**
     * @brief Удаляет графический примитив из сцены по его уникальному идентификатору.
     *
     * Производит поиск объекта с заданным ID внутри контейнера и удаляет его.
     *
     * @param id Уникальный числовой идентификатор (ID) примитива, который необходимо удалить.
     * @return true Если объект с таким ID был найден и успешно удален.
     * @return false Если объект с указанным ID на сцене отсутствует.
     */
    bool RemovePrimitive(uint32_t /*id*/)
    {
        // TODO Удалить элемент по id
        return true;
    }

    /**
     * @brief Возвращает константную ссылку на список всех примитивов сцены.
     *
     * Позволяет внешним компонентам (например, `ViewModel` или `JsonDoc`)
     * безопасно читать и обходить элементы сцены без возможности их случайной модификации.
     *
     * @return Константная ссылка на `std::vector<std::shared_ptr<IPrimitive>>`.
     */
    const std::vector<std::shared_ptr<IPrimitive>>& GetPrimitives() const
    {
        return primitives;
    }
};
