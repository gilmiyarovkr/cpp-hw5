#pragma once

#include <string>
#include <memory>
#include <functional>
#include <unordered_map>

#include "SceneModel.h"
#include "IDocIO.h"

//----------------------------------------------------------------------------------------
/**
 * @class ViewModel
 * @brief Класс, отвечающий за визуализацию (рендеринг) графических примитивов.
 *
 * Данный класс представляет собой слой представления (View) в паттерне MVC.
 * Он выводит текущее состояние графической сцены на экран или в консоль.
 */
class ViewModel
{
public:
    /**
     * @brief Отображает все графические примитивы, находящиеся на сцене.
     * @param scene Ссылка на константную модель сцены для отрисовки.
     */
    void Render(const SceneModel& scene);
};

//----------------------------------------------------------------------------------------
/**
 * @class Controller
 * @brief Класс контроллера, управляющий логикой приложения и связывающий модель с представлением.
 *
 * Реализует слой управления (Controller) в паттерне MVC. Координирует процессы
 * создания, удаления объектов, а также импорта и экспорта сцены через различные форматы файлов.
 */
class Controller
{
public:
    /**
     * @brief Синоним типа для фабрики стратегий ввода-вывода.
     *
     * Представляет собой функциональный объект, возвращающий уникальный указатель
     * на объект стратегии сохранения/загрузки документов (`IDocIO`).
     */
    using StrategyFactory = std::function<std::unique_ptr<IDocIO<SceneModel>>()>;

private:
    SceneModel m_scene; ///< Текущая модель сцены, содержащая примитивы.
    ViewModel m_view;   ///< Объект представления для визуализации изменений.
    std::unordered_map<std::string, StrategyFactory> m_io_factories; ///< Карта зарегистрированных фабрик форматов ввода-вывода (ключ — расширение файла).

    /**
     * @brief Извлекает расширение из имени файла.
     * @param fileName Полное имя файла или путь к нему.
     * @return Строка, содержащая расширение файла (включая точку, например, ".json").
     */
    std::string GetExtension(const std::string& fileName) const;

public:
    /**
     * @brief Конструктор по умолчанию.
     */
    Controller() = default;

    /**
     * @brief Деструктор по умолчанию.
     */
    ~Controller() = default;

    /**
     * @brief Регистрирует новую стратегию ввода-вывода для определенного расширения файла.
     * @param ext Расширение файла (например, ".json", ".csv").
     * @param factory Фабричная функция для создания соответствующего объекта `IDocIO`.
     */
    void RegisterFormat(const std::string& ext, StrategyFactory factory);

    /**
     * @brief Загружает сцену из указанного файла.
     *
     * Метод автоматически определяет нужный формат по расширению файла
     * и использует зарегистрированную фабрику для чтения данных.
     * @param fileName Путь к загружаемому файлу.
     */
    void HandleLoadFile(const std::string& fileName);

    /**
     * @brief Сохраняет текущую сцену в указанный файл.
     *
     * Метод автоматически подбирает стратегию сохранения на основе расширения файла.
     * @param fileName Путь к файлу для сохранения.
     */
    void HandleSaveFile(const std::string& fileName);

    /**
     * @brief Удаляет графический примитив из сцены по его уникальному идентификатору.
     *
     * После успешного удаления автоматически вызывает обновление представления (`Render`).
     * @param id Уникальный числовой идентификатор (ID) удаляемого примитива.
     */
    void DeletePrimitive(uint32_t id);

    /**
     * @brief Возвращает константную ссылку на текущую модель сцены.
     * @return Константная ссылка на `SceneModel`.
     */
    const SceneModel& GetScene() const
    {
        return m_scene;
    }

    /**
     * @brief Добавляет готовый графический примитив на сцену.
     *
     * После добавления объекта на сцену автоматически инициирует
     * перерисовку интерфейса с помощью `ViewModel`.
     * @param primitive Умный указатель `std::shared_ptr` на добавляемый графический примитив.
     */
    void CreatePrimitive(std::shared_ptr<IPrimitive> primitive);
};
