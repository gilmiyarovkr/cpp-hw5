#pragma once

#include <cstdint>

class Line;
class Circle;

/**
 * @class IPrimitiveVisitor
 * @brief Интерфейс Посетителя для реализации операций над графическими примитивами.
 *
 * Является частью паттерна проектирования "Посетитель" (Visitor). Позволяет добавлять
 * новые операции (например, сериализацию в JSON/CSV, подсчет площади) для существующих
 * классов примитивов без изменения их собственного кода.
 */
class IPrimitiveVisitor
{
public:
    /**
     * @brief Виртуальный деструктор по умолчанию.
     */
    virtual ~IPrimitiveVisitor() = default;

    /**
     * @brief Метод для обработки (посещения) объекта типа Line.
     * @param line Константная ссылка на посещаемый объект линии.
     */
    virtual void Visit(const Line& line) = 0;

    /**
     * @brief Метод для обработки (посещения) объекта типа Circle.
     * @param circle Константная ссылка на посещаемый объект окружности.
     */
    virtual void Visit(const Circle& circle) = 0;
};

/**
 * @class IPrimitive
 * @brief Абстрактный базовый класс для всех графических примитивов.
 *
 * Задает общий интерфейс для отрисовки, идентификации и взаимодействия с паттерном Visitor.
 * Автоматически генерирует уникальный числовой идентификатор для каждого созданного примитива.
 */
class IPrimitive
{
private:
    uint32_t m_id;                     ///< Уникальный числовой идентификатор примитива.
    static inline uint32_t m_next_id = 1; ///< Статический счетчик для генерации уникальных ID (инициализируется единицей).

public:
    /**
     * @brief Конструктор по умолчанию.
     *
     * Присваивает объекту текущее значение счетчика `m_next_id` и увеличивает его на 1.
     */
    IPrimitive() : m_id(m_next_id++) {}

    /**
     * @brief Виртуальный деструктор по умолчанию.
     *
     * Необходим для безопасного удаления объектов производных классов через указатель на базовый класс.
     */
    virtual ~IPrimitive() = default;

    /**
     * @brief Возвращает уникальный идентификатор примитива.
     * @return 32-битное беззнаковое целое число (ID).
     */
    uint32_t GetID() const { return m_id; }

    /**
     * @brief Виртуальный метод для отрисовки графического примитива.
     *
     * Должен быть переопределен в конкретных классах для вывода информации в GUI или консоль.
     */
    virtual void Draw() const = 0;

    /**
     * @brief Принимает посетителя для выполнения внешней операции над примитивом.
     *
     * Реализует механизм двойной диспетчеризации. Внутри переопределенного метода
     * производного класса вызывается `visitor.Visit(*this);`.
     *
     * @param visitor Ссылка на объект посетителя, выполняющего операцию.
     */
    virtual void Accept(IPrimitiveVisitor& visitor) const = 0;
};
