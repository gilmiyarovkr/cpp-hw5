#pragma once

#include <string>
#include <optional>

/**
 * @class IDocIO
 * @brief Абстрактный интерфейсный класс для операций импорта и экспорта документов.
 *
 * Определяет контракт для классов, реализующих сохранение и загрузку
 * данных определенного типа.
 *
 * @tparam DataType Тип данных, с которым работают операции ввода-вывода
 */
template <typename DataType>
class IDocIO
{
public:
    /**
     * @brief Виртуальный деструктор по умолчанию.
     *
     * Гарантирует корректное уничтожение объектов производных классов
     * через указатель на базовый интерфейс.
     */
    virtual ~IDocIO() = default;

    /**
     * @brief Загружает данные из указанного файла.
     *
     * Переопределяется в конкретных стратегиях (например, JsonDoc, CsvDoc)
     * для парсинга файлов соответствующего формата.
     *
     * @param file Путь к файлу, из которого необходимо считать данные.
     * @return std::optional<DataType> Содержит объект данных в случае успешной загрузки,
     *                                 или std::nullopt, если чтение файла завершилось ошибкой.
     */
    virtual std::optional<DataType> Load(const std::string& file) = 0; // Загрузить

    /**
     * @brief Сохраняет переданные данные в указанный файл.
     *
     * Переопределяется в конкретных стратегиях для сериализации данных в нужный формат.
     *
     * @param file Путь к файлу, в который будет произведена запись.
     * @param data Константная ссылка на сериализуемый объект данных.
     * @return true Если файл успешно сохранен.
     * @return false Если в процессе сохранения произошла ошибка.
     */
    virtual bool Save(const std::string& file, const DataType& data) = 0; // Сохранить
};
